// crear variables para el texto oculto
document.addEventListener('DOMContentLoaded', function () {
    const btn_oculto_texto = document.getElementById('oculto_texto_btn');
    const oculto_texto = document.getElementById('oculto_texto');

    btn_oculto_texto.addEventListener('click', function() {
        oculto_texto.classList.toggle('show');
        if (oculto_texto.classList.contains('show')) {
            btn_oculto_texto.innerHTML = 'Ver Menos ...';
        } else {
            btn_oculto_texto.innerHTML = 'Ver Más ...';
        }
    });
});

